#include <iostream>
using namespace std;
int main(){
    int a;
    cout<<"size of integer "<<sizeof(a)<<endl;
    
    bool b;
    cout<<"size of boolean "<<sizeof(b)<<endl;

    float c;
    cout<<"size of float "<<sizeof(c)<<endl;

    char d;
    cout<<"size of character "<<sizeof(d)<<endl;

    double e;
    cout<<"size of double "<<sizeof(e)<<endl;
    
    return 0;

}